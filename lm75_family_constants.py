"""
lm75_family_constants.py
Константы и параметры для всего семейства температурных датчиков:
LM75, LM75A/B/C/D, TMP75, TMP102, TMP117/TMP119.
"""
from micropython import const

# =============================================================================
# БАЗОВЫЕ КОДЫ РАЗРЕШЕНИЙ (9, 10, 11, 12 бит)
# Соответствуют битам R1:R0 в регистре конфигурации
# =============================================================================
RES_9BIT: int = const(0)
RES_10BIT: int = const(1)
RES_11BIT: int = const(2)
RES_12BIT: int = const(3)

# Таблица LSB (вес младшего разряда) для настраиваемых разрешений
# Индекс таблицы = код разрешения (0..3)
RES_LSB_TABLE: tuple[float, ...] = (0.5, 0.25, 0.125, 0.0625)

# =============================================================================
# СПЕЦИАЛЬНЫЕ ЗНАЧЕНИЯ ДЛЯ TMP11X (16 БИТ)
# TMP117/TMP119 имеют фиксированное разрешение, выходящее за рамки 0..3
# =============================================================================
TMP11X_RESOLUTION_CODE: int = const(16)  # Условный код для 16 бит
TMP11X_LSB: float = const(0.0078125)  # 2^-7 гр. Ц.

# =============================================================================
# LM75 (ОРИГИНАЛЬНЫЙ, LEGACY)
# Фиксированное разрешение 9 бит. Низкая точность.
# =============================================================================
LM75_RESOLUTION_CODE: int = const(RES_9BIT)
LM75_LSB: float = const(0.5)

LM75_ACCURACY_TYP: float = const(2.0)  # +/-2.0 гр. Ц. (типичная)
LM75_ACCURACY_MAX: float = const(3.0)  # +/-3.0 гр. Ц. (максимальная)
LM75_TEMP_MIN: float = const(-55.0)
LM75_TEMP_MAX: float = const(125.0)

# =============================================================================
# TMP75 / LM75A / B / C / D / TMP175 / TMP275
# Настраиваемое разрешение (9..12 бит).
# Базовый класс в драйвере - TMP75.
# =============================================================================
TMP75_RESOLUTION_MIN: int = const(RES_9BIT)
TMP75_RESOLUTION_MAX: int = const(RES_12BIT)
TMP75_RESOLUTION_DEFAULT: int = const(RES_9BIT)

# Точность согласно Table 6.5 (Datasheet TMP75)
# Типичная точность для полного диапазона -55..+125 гр. Ц. составляет +/-1.0 гр. Ц.
TMP75_ACCURACY_TYP: float = const(1.0)
TMP75_ACCURACY_MAX: float = const(2.0)
TMP75_TEMP_MIN: float = const(-55.0)
TMP75_TEMP_MAX: float = const(125.0)

# =============================================================================
# TMP102
# Фиксированное разрешение 12 бит. Улучшенная точность.
# Диапазон сужен относительно других датчиков.
# =============================================================================
TMP102_RESOLUTION_CODE: int = const(RES_12BIT)
TMP102_LSB: float = const(0.0625)

TMP102_ACCURACY_TYP: float = const(0.5)
TMP102_ACCURACY_MAX: float = const(2.0)
TMP102_TEMP_MIN: float = const(-40.0)  # Внимание: диапазон начинается с -40
TMP102_TEMP_MAX: float = const(125.0)

# =============================================================================
# TMP11X (TMP117 / TMP119)
# Высокая точность, 16 бит, отдельная архитектура.
# =============================================================================
TMP11X_LSB: float = const(TMP11X_LSB)  # Дублируем для совместимости имен
TMP11X_ACCURACY_TYP: float = const(0.1)  # +/-0.1 гр. Ц. (высокоточный режим)
TMP11X_ACCURACY_MAX: float = const(0.3)
TMP11X_TEMP_MIN: float = const(-55.0)
TMP11X_TEMP_MAX: float = const(150.0)  # Расширенный диапазон до 150 гр. Ц.


# =============================================================================
# ADT7410 (Analog Devices)
# Высокая точность, 13/16 бит, отдельная логика режимов
# =============================================================================
ADT7410_ACCURACY_TYP: float = const(0.5)
ADT7410_ACCURACY_MAX: float = const(2.0)
ADT7410_TEMP_MIN: float = const(-55.0)
ADT7410_TEMP_MAX: float = const(150.0)

# Режимы работы (Биты 6:5 регистра конфигурации)
ADT7410_MODE_CONTINUOUS: int = const(0x00)
ADT7410_MODE_ONE_SHOT:  int = const(0x20)  # Bit 5 = 1
ADT7410_MODE_1SPS:      int = const(0x40)  # Bit 6 = 1
ADT7410_MODE_SHUTDOWN:  int = const(0x60)  # Bits 6:5 = 11

# Разрешение
ADT7410_RES_13BIT: int = const(0)  # 0.0625 гр. Ц.
ADT7410_RES_16BIT: int = const(1)  # 0.0078125 гр. Ц.
ADT7410_LSB_13: float = const(0.0625)
ADT7410_LSB_16: float = const(0.0078125)

# =============================================================================
# ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
# =============================================================================

def resolution_to_lsb(code: int) -> float:
    """
    Преобразует код разрешения в значение LSB.

    Args:
        code: 0=9bit, 1=10bit, 2=11bit, 3=12bit, 16=16bit (TMP11X)
    Returns:
        float: Значение LSB в гр. Ц..
    """
    if code == TMP11X_RESOLUTION_CODE:
        return TMP11X_LSB
    if RES_9BIT <= code <= RES_12BIT:
        return RES_LSB_TABLE[code]
    raise ValueError(f"Unknown resolution code: {code}")
